#include <stdio.h>
#include <stdlib.h>

int main(void)
{

    int i, j;   //sets data type for variables i and j to integer
    float x, y; //sets data type for variables x and y to float

    //assigning respective values of the different data types
    i = 10;
    j = 20;
    //f indicates the data type as float
    x = 43.2892f;
    y = 5527.0f;

    //printing the result, data values listed chronologically
    printf("i = %d, j = %d, x = %f, y = %f\n", i, j, x, y);

    return 0;
}
